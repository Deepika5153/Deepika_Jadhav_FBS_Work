# 3. Program to find quotient and remainder of two numbers.
# for quotient use - /
# for reminder use - %

num1 = int(input('Enter Dividend : '))
num2 = int(input('Enter Divisor : '))

quotient = num1 / num2
print(f'quotient is - {quotient}')

remainder = num1 % num2
print(f'remainder is - {remainder}')
